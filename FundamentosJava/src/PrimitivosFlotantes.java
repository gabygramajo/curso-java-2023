public class PrimitivosFlotantes {
    public static void main(String[] args) {
        System.out.println("Datos Primitivos - ENTEROS");

        //FLOAT
        float nroFloat = 3.1416F;
        System.out.println("\nnroFloat = " + nroFloat);
        System.out.println("El tipo Float corresponde en Bytes a = " + Float.BYTES);
        System.out.println("El tipo Float corresponde en Bits a = " + Float.SIZE);
        System.out.println("Valor Max en tipo Float = " + Float.MAX_VALUE);
        System.out.println("Valor Min en tipo Float = " + Float.MIN_VALUE);

        //SHORT
        double nroDouble = 3.1416;
        System.out.println("\nnroDouble = " + nroDouble);
        System.out.println("El tipo Double corresponde en Bytes a = " + Double.BYTES);
        System.out.println("El tipo Double corresponde en Bits a = " + Double.SIZE);
        System.out.println("Valor Max en tipo Double = " + Double.MAX_VALUE);
        System.out.println("Valor Min en tipo Double = " + Double.MIN_VALUE);
    }
}
