public class PrimitivosEnteros {

    public static void main(String[] args) {
        System.out.println("Datos Primitivos - ENTEROS");

        //BYTE
        byte nroByte = 127;
        System.out.println("\nnroByte = " + nroByte);
        System.out.println("El tipo Byte corresponde en Bytes a = " + Byte.BYTES);
        System.out.println("El tipo Byte corresponde en Bits a = " + Byte.SIZE);
        System.out.println("Valor Max en tipo Byte = " + Byte.MAX_VALUE);
        System.out.println("Valor Min en tipo Byte = " + Byte.MIN_VALUE);

        //SHORT
        short nroShort = 32767;
        System.out.println("\nnroShort = " + nroShort);
        System.out.println("El tipo Short corresponde en Bytes a = " + Short.BYTES);
        System.out.println("El tipo Short corresponde en Bits a = " + Short.SIZE);
        System.out.println("Valor Max en tipo Short = " + Short.MAX_VALUE);
        System.out.println("Valor Min en tipo Short = " + Short.MIN_VALUE);

        //INT
        int nroInt = 2147483647;
        System.out.println("\nnroInt = " + nroInt);
        System.out.println("El tipo Int corresponde en Bytes a = " + Integer.BYTES);
        System.out.println("El tipo Int corresponde en Bits a = " + Integer.SIZE);
        System.out.println("Valor Max en tipo Int = " + Integer.MAX_VALUE);
        System.out.println("Valor Min en tipo Int = " + Integer.MIN_VALUE);

        //Long
        long nroLong = 9223372036854775807L;
        System.out.println("\nnroLong = " + nroByte);
        System.out.println("El tipo Long corresponde en Bytes a = " + Long.BYTES);
        System.out.println("El tipo Long corresponde en Bits a = " + Long.SIZE);
        System.out.println("Valor Max en tipo Long = " + Long.MAX_VALUE);
        System.out.println("Valor Min en tipo Long = " + Long.MIN_VALUE);

    }
}
